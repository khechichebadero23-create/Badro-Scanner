import requests

# دمج اللمسة العربية 
مطبعة = print
مدخل = input

# اعتماد الاسم التقني الجديد لحماية الخصوصية
اسم_الأداة = "ShadowScan-DirFinder"

مطبعة(f"--- أداة {اسم_الأداة} لاكتشاف المجلدات المخفية ---")

# 1. نطلب رابط الموقع المستهدف
الموقع = مدخل("أدخل رابط الموقع (مثال: http://example.com): ")

if not الموقع.startswith("http"):
    الموقع = "http://" + الموقع

# 2. قائمة المجلدات الشائعة للتخمين
المجلدات_السرية = [
    "admin", "login", "wp-admin", "dashboard", "backup", 
    "db", "database", "secret", "config", "api", "uploads"
]

مطبعة(f"\n[+] تم تحديد الهدف: {الموقع}")
مطبعة(f"[+] جاري بدء فحص المسارات عبر {اسم_الأداة}... انتظر...\n")

try:
    for مجلد in المجلدات_السرية:
        الرابط_الكامل = f"{الموقع}/{مجلد}"
        
        # إرسال طلب لفحص وجود المجلد
        الاستجابة = requests.get(الرابط_الكامل, timeout=2.0)
        
        if الاستجابة.status_code == 200:
            مطبعة(f"🔓 [موجود] ---> {الرابط_الكامل}")
        elif الاستجابة.status_code == 403:
            مطبعة(f"🔒 [محمي] ---> {الرابط_الكامل}")
            
except requests.exceptions.RequestException:
    مطبعة("\n[X] خطأ: تعذر الاتصال بالموقع، تأكد من الرابط أو الإنترنت.")
except KeyboardInterrupt:
    مطبعة("\n[!] تم إيقاف الفحص.")

مطبعة(f"\n--- انتهت عملية الفحص بنجاح عبر {اسم_الأداة} ---")

