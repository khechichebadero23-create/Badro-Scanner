import socket

# تعريف الدوال باللغة العربية (هذا الجزء مسموح به تماماً ويدعمه بايثون)
مطبعة = print
مدخل = input

مطبعة("--- أداة بادرو-Nmap لفحص الشبكات والمنافذ ---")

# 1. نطلب من المستخدم رابط الموقع
الموقع_المستهدف = مدخل("أدخل رابط الموقع (مثال: google.com): ")

try:
    # 2. تحويل الرابط إلى عنوان IP حقيقي
    عنوان_IP = socket.gethostbyname(الموقع_المستهدف)
    مطبعة(f"\n[+] تم العثور على عنوان الـ IP الخاص بالموقع: {عنوان_IP}")
    مطبعة("[+] جاري بدء فحص المنافذ الحساسة... انتظر قليلاً...\n")
    
    # 3. قائمة بالمنافذ الشهيرة التي نريد فحصها
    المنافذ_المستهدفة = [21, 22, 80, 443, 8080]
    
    for منفذ in المنافذ_المستهدفة:
        # إنشاء اتصال شبكي سريع (طرق الباب)
        مقبس_الاتصال = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
        مقبس_الاتصال.settimeout(1.0) # انتظر ثانية واحدة فقط
        
        # محاولة الاتصال بالمنفذ
        النتيجة = مقبس_الاتصال.connect_ex((عنوان_IP, منفذ))
        
        # إذا كانت النتيجة 0، يعني الباب مفتوح!
        if النتيجة == 0:
            مطبعة(f"🔓 المنفذ [{منفذ}] ---> مـفـتـوح (Open) 🔥")
        else:
            مطبعة(f"🔒 المنفذ [{منفذ}] ---> مغلق (Closed)")
            
        مقبس_الاتصال.close()

except socket.gaierror:
    مطبعة("\n[X] خطأ: لم أتمكن من العثور على الموقع. تأكد من الرابط أو اتصال الإنترنت.")
except KeyboardInterrupt:
    مطبعة("\n[!] تم إيقاف الفحص بواسطة المستخدم.")

مطبعة("\n--- تم الانتهاء من الفحص بنجاح يا قائد بادرو ---")

